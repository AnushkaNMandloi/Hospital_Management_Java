    private void populateTable() {
        System.out.println("inside populate table");
        // System.out.println(patient.getEncounterHistory().getData().get(0).getVitalSign());
        DefaultTableModel model = (DefaultTableModel) encounterTable.getModel();
        model.setRowCount(0);
        if (patient == null) {
            return;
        }
        for (Encounter entry : patient.getEncounterHistory().getData()) {   
            Object[] row = new Object[3];
            row[0] = patient.getPatientId();
            row[1] = entry.getDate();
            row[2] = entry.getVitalSign().getName();
            model.addRow(row);
        }
    }
